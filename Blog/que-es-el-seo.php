 <h1 class="mb-5 mt-5 wow bounceInLeft tamaño-titulo-blog1" data-wow-duration="3s"> ¿Qué es el SEO y porque lo necesita tu sitio web?</h1>
 <p class="mb-5 text-justify tamaño-contenido-blog1">SEO es el acrónimo de Search Engine Optimization. El objetivo principal del SEO es darle visibilidad a tu sitio web haciendo que aparezca en google, lo que generará tráfico hacia tu sitio, haciendo que este genere más beneficios, un sitio web sin SEO no sirve, ya que no cumplirá los objetivos con los que se creó inicialmente. Sin embargo, el SEO no es solo este apartado, en este post explicaremos más a fondo ¿Qué es el SEO?
</p>
 <p class="mb-5 text-justify tamaño-contenido-blog1">Un estudio realizado por parte de Sistrix, que está basado en más de 124 millones de clicks, demuestra que el primer resultado de Google se lleva más del 60% de clicks en una búsqueda, por lo que nuestro objetivo sera llegar a esa primera posición.
</p>
 <p class="text-justify tamaño-contenido-blog1">El trabajo del SEO se puede dividir en dos grupos, el SEO On-site y el SEO Off-site, te explicaremos a detalle en qué se diferencian el uno del otro.
</p>
 <p class="text-justify tamaño-contenido-blog1">SEO On-site: Es el SEO que se trabaja directamente en el sitio web, aquí hay varios apartados que trabajar, velocidad de carga de la página, el uso de keywords en el meta description del sitio para que el motor de búsqueda entienda de qué se trata la página y que de esa forma se pueda catalogar, pureza del código, y uso de las URLs. Con este trabajo un sitio web ya puede empezar a subir, pero su curva de crecimiento será lenta.
</p>

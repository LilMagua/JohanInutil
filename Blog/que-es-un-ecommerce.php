 <h1 class="mb-5 mt-5 wow bounceInLeft tamaño-titulo-blog1" data-wow-duration="3s"> ¿Qué es un e-commerce?</h1>
 <p class="mb-5 text-justify tamaño-contenido-blog1">O comercio electrónico, es un sitio web que tiene como objetivo principal automatizar ventas mediante pasarelas de pago. ¿Qué ventajas tiene? ¿Cómo funciona?
</p>
 <p class="mb-5 text-justify tamaño-contenido-blog1">En este artículo explicaremos estas preguntas para que no hayan dudas sobre este tipo de sitios web.</p>
 <p class="text-justify tamaño-subtitulo-blog1">¿Qué ventajas ofrece?
</p>
 <p class="text-justify tamaño-contenido-blog1">Comparar un E-commerce con un modelo de negocio tradicional hace que nos demos cuenta de la gran cantidad de ventajas que puede llegar a tener un sitio web de estas características.
</p>
<div class="text-center">
  <img src="../img/Articulo4.png" alt="Articulo4" class="my-5 imagenblog22" title="Articulo4Imagen">
</div>
 <p class="mb-5 text-justify tamaño-contenido-blog1">En cuanto al tiempo ofrece una gran ventaja ya que tu tienda en línea estará disponible 24/7 sin descanso, de esa forma tus clientes podrán acceder al catálogo de tus productos en cualquier momento. Sabemos que ahorraremos tiempo por venta individual ya que nos encargaremos únicamente del envío y se acorta el proceso de venta en comparación a un local físico. 
</p> 
 <p class="mb-5 text-justify tamaño-contenido-blog1">Podrás llegar a más gente ya que un sitio web tiene más alcance que un establecimiento físico, ya que puede llegar a otras ciudades incluso otros países, por consiguiente tener más clientes potenciales, debido a la flexibilidad que tiene un sitio web para ser visto desde diferentes partes, podremos tener más clientes a diferencia de los que tendríamos en un establecimiento físico.
</p>
 <p class="mb-5 text-justify tamaño-contenido-blog1">Reducción de costes, volviendo a mencionar el tema del establecimiento físico, estaremos llegando a más gente sin tener que pagar el espacio físico de un local, solo necesitaremos nuestro alojamiento de hosting, que aqui en Colombia puede tener un coste aproximado de $120.000 pesos ($32.00 Dólares), por el contrario la renta de un local físico cuesta aproximadamente $1.000.000 de pesos ($268.00 Dólares) además de esto no tendremos que pagar tanto personal para atender nuestro negocio, ya que nos tendremos que ocupar solo de enviar los pedidos a sus respectivos compradores.
</p>

 <p class="mb-5 text-justify tamaño-contenido-blog1">Mejor imagen corporativa, ya que reflejaras más seriedad y confianza a la hora de dar a conocer tu producto o servicio. 
</p>

 <p class="mb-5 text-justify tamaño-subtitulo-blog1">¿Es seguro?</p>

 <p class="mb-5 text-justify tamaño-contenido-blog1">En Colombia aún no se ve con confianza el hecho de comprar en internet, esto podemos saberlo viendo las estadísticas y comparándolas con otros países, en china, las ventas en línea son el 28,6% de las ventas en total, mientras que en Colombia es del 6,8% según un estudio de Semana. Esto se debe a la falta de seguridad que las personas creen que ofrece un servicio como este, pero esta falta de seguridad está justificada, ya que hoy en dia las ventas en e-commerce son muy seguras y eso se ve reflejado en las cifras que vimos anteriormente, este es un mercado que seguirá creciendo en los próximos años.

</p>

 <p class="mb-5 text-justify tamaño-subtitulo-blog1">¿Cómo funciona?</p>
 <p class="mb-5 text-justify tamaño-contenido-blog1">El objetivo de un E-Commerce es automatizar las ventas, por ello, estos sitios web cuentan con la funcionalidad de lo que se conoce como “Pasarela de pago”.
</p>
 <p class="mb-5 text-justify tamaño-contenido-blog1">Una pasarela de pago es un medio por el cual se le permite al cliente pagar un producto de forma totalmente online. El modo de uso de un E-Commerce comenzaría en su catálogo, el cliente escoge qué productos desea comprar, lo re direcciona a un “Carrito de compra” en donde se almacenarán todos sus pedidos, para proceder a pagar con la pasarela de pago, de esta manera el pago llegará al encargado de envíos online de la empresa para que pueda hacer el procedimiento correspondiente.

</p>

 <p class="mb-5 text-justify tamaño-contenido-blog1">Si tienes preguntas acerca del E-Commerce, <a href="https://wa.me/c/573126663512">contáctanos.</a> 

 </p>


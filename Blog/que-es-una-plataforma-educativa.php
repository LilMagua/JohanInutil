 <h1 class="mb-5 mt-5 wow bounceInLeft tamaño-titulo-blog1" data-wow-duration="3s"> ¿Qué es una plataforma educativa o LMS?</h1>
 <p class="mb-5 text-justify tamaño-contenido-blog1">Es un sitio web que ofrece entornos virtuales para facilitar el aprendizaje a los alumnos por medio de internet, no solo se emplea para ambientes educativos específicamente como colegios o universidades, también se utilizan para ambientes laborales en la capacitación de empleados. Se utilizan varias herramientas y facilidades para llevar esto a cabo. Se necesita una gestión de los cursos, los certificados y los usuarios para que funcione correctamente.
                            
</p>
 <p class="mb-5 text-justify tamaño-subtitulo-blog1">¿Qué herramientas se utilizan?</p>
 <p class="text-justify tamaño-contenido-blog1">Herramientas de gestión de contenidos: le permite al docente enviar al aprendiz contenido, ya sean archivos de texto, imágenes o videos que estarán organizados para el correcto funcionamiento de la plataforma.
                           
</p>
 <p class="text-justify tamaño-contenido-blog1">Herramientas de comunicación y colaboración: En esta función se encuentran los foros y los chats, ya que es muy importante darle a la comunidad la posibilidad de debatir y conversar entre ellos para favorecer el ambiente de aprendizaje.

</p>
<div class="text-center">
  <img src="../img/Articulo5.png" alt="Articulo5" class="my-5 imagenblog22">
</div>
 <p class="mb-5 text-justify tamaño-contenido-blog1">Herramientas de seguimiento y evaluación: Este es un elemento que solo estará a disposición de los docentes o los lideres de la plataforma, se suelen utilizar planillas en los que se puede valorar el rendimiento del aprendiz con una nota.</p>
                            
</p> 
 <p class="mb-5 text-justify tamaño-contenido-blog1">Herramientas de administración y asignación de permisos: La forma mas efectiva de conseguir esto es con un sistema de registro e inicio de sesión, en el que dependiendo de qué tipo de cuenta disponga la persona tendrá ciertas funcionalidades, por ejemplo, no será lo mismo una cuenta de docente que una cuenta de aprendiz.
                            
</p>
 <p class="mb-5 text-justify tamaño-contenido-blog1">Herramientas complementarias: Este apartado es específico para lo que se busque hacer con la plataforma, si necesitas un buscador para los cursos o si necesitas una sección de juegos en tu sitio, todas estas serian herramientas complementarias.
                           
</p>

 <p class="mb-5 text-justify tamaño-subtitulo-blog1">¿Necesitas una plataforma virtual?
</p>

 <p class="mb-5 text-justify tamaño-contenido-blog1">Si tu objetivo con un sitio web es tener un espacio estructurado y formalizado en el que se logre impartir de forma correcta el conocimiento, la respuesta es sí, sin embargo, estas plataformas educativas suelen ser proyectos de presupuestos bastante altos respecto a otro tipo de sitios web, es algo para tener en cuenta si quieres crear tu propia plataforma educativa.
</p>

 <p class="mb-5 text-justify tamaño-contenido-blog1">En un estudio realizado por la institución de educación superior San Luis Potosí en 2017 después de implementar una plataforma educativa, se afirma que en una encuesta hecha a los estudiantes, se llegó a la conclusión de que la mejora en aspectos como el logro de objetivos, la distribución de tiempo y la participación de los alumnos alcanzó un 75%. Por parte de los docentes el 92% considero que la utilización de una plataforma virtual ha facilitado muchos apartados en su tarea de compartir el conocimiento.
</p>

